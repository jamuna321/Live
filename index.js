    const toggleBtn = document.getElementById('darkModeToggle');
    const body = document.body;

    toggleBtn.addEventListener('click', () => {
      if (body.classList.contains('light-mode')) {
        body.classList.replace('light-mode', 'dark-mode');
          toggleBtn.classList.add("active")
      } else {
        body.classList.replace('dark-mode', 'light-mode');
          toggleBtn.classList.remove("active")
      }
    });

function createChannelList(id, buttonElement) {
   const channelListContainer = document.querySelector('.channel_list_container');
   channelListContainer.innerHTML = "";
   const channelData = data[id] || [];

   // Remove 'active' class from all buttons
   const allButtons = document.querySelectorAll('.channel');
   allButtons.forEach(button => button.classList.remove('active'));
   
   // Add 'active' class to the clicked button
   buttonElement.classList.add('active');

   const channelInfo = channelData.map(channel => `
       <button class="channel_list_link_button" onclick="changeIframeSource('${channel.link}', this)">
           ${channel.channel_name}
       </button>
   `).join('');

   channelListContainer.innerHTML = `<h3>${id}</h3>`;
   const dataDiv = document.createElement('div');
   dataDiv.className = 'data';
   dataDiv.innerHTML = channelInfo;

   channelListContainer.appendChild(dataDiv);
}

function changeIframeSource(link, buttonElement) {
   const iframe = document.getElementById('stream-iframe');
   iframe.src = link; // Update the iframe source to the clicked channel link

   // Remove 'active' class from all channel list buttons
   const allChannelButtons = document.querySelectorAll('.channel_list_link_button');
   allChannelButtons.forEach(btn => btn.classList.remove('active'));

   // Add 'active' class to the clicked button
   buttonElement.classList.add('active');
}
